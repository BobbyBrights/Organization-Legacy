<h1>Every Drop Counts.</h1>
<h2>The Project Water Experiment</h2>
<br>

<p>I stink at dodgeball. In fact, I haven’t played a single game at the tournaments. It’s a funny truth that goes to show it’s not as much about the game for me as it is about the meaning behind it. Here’s why I spent more time in high school on this little project than almost anything else:</p>

<p>When I was 8 years old, I remember riding in a packed car down a dirt road in the middle of Kerala, India. As we came to a standstill in heavy traffic, a hand was shoved through my window and opened right in front of me. A girl, young enough to be in college, was carrying a baby. Begging down these streets full of commuters towards the city was the best chance she had to feed her daughter. As I looked up towards the street, I realized she wasn’t the only one. Dozens of other women were doing the same thing. Later, I remember seeing a boy no older than 8 or 9 digging through piles of trash with his little sister. As the two dug through the filth, you could see the desperation in their eyes.</p>

<p>There is a weight to knowledge. When you have it, you can no longer say you don't know. The most visionary people in the world feel BOTHERED, and vision always begins with a cry, a passion. Here’s something I learned- When looking for your passion, pay attention to your tears. For me, I knew there was a great need for clean water and aid to developing villages around the world, and seeing that need broke my heart. But passion is not enough. Passion must become vision. If all you have is passion, you will never change the world.</p>

<p>Project Water started 4 years ago as a social experiment. You all know dodgeball is about far more than playing a classic playground game and getting out of school all day. It became a venue for sharing hard truths about the world we live in, outside the “Wexford Bubble”. My question was, what would people do with the knowledge we shared? We wanted people to leave asking questions they hadn't considered before. However, getting people to that point, especially during a hectic day of dodgeball, is challenging. People hate paradigm shifts because they are forced to start over at square one and learn again. Our guest speakers, and Project Experience, were designed to help ease people out of their own shoes, and into the experience of others. Many people saw the obvious; that there is a global need for clean water. But moving beyond that, understanding the whole cycle it perpetuates, and the redemptive process that leads to freedom, is what we aimed to share. If someone is able to consider the full weight of these truths for even one minute, it can change everything. For me, it boils down to this quote; “Expanded influence is the inevitable passion and response of those who've aligned themselves with eternity on their hearts.” That’s why I keep doing dodgeball.</p>

<p>With that in mind, your generous donations have also had serious impact around the world, which is something to celebrate! Since Project Water started, we’ve funded:

<p>In Kyenjojo, Uganda:<br>
-1 Water Harvesting System for a school<br>
-1 Septic Tank System for a Medical Center<br>
-1 Septic Tank System for a School Dormitory<br>
-1 Community Bore Hole Well<br>
-1 4-Bedroom House for Homeless Families</p>
 
<p>Location Undisclosed (For security reasons, until a later date):<br>
-1 Mechanized Clean Water Well<br>
-Other global clean water initiatives</p>

<p>Many people in Africa have to walk the distance from NASH to downtown Pittsburgh to collect clean water. These gifts change lives beyond our years in high school. These gifts change everything. And it wouldn't have been possible without your support.</p>

<p>Here are some numbers:</p>

<p>The first dodgeball tournament we hosted had over 150 players. With this year’s tournament, we were over 900. That’s a 600% increase over just three years. </p>

<p>Our events have hosted over 2000 players and volunteers, and generated over $36,000.00. </p>

<p>This wouldn’t have been possible without any of my <a href="http://naprojectwater.com/team" target="_blank">team members</a>, as well as the original group, Dave Bjorklund, Ben Cinker, MJ Barton, Jacob Greco, Bailey Daftary, Oscar Rzodkiewicz, Brendan Grzyb, Danny Fujito, Julie Chen, Shane Mitnick, and Alex Taffe. Nor would it have been possible without the endless support of Mr. Hull, Mrs. Mayhew, Mrs. Traversari, Ms. Stackhouse, Mr. Quinerly, Mrs. Ewing, Mr. Paylo, Mrs. Prosser, Dr. Hyland, Mr. Kreider, Mrs. Testa, and Dr. Scherrer. These staff members, among many more, believed in me, and my team, at every step in our journey. Knowing someone believes in you is one of the most empowering gifts an adult can give, so I thank each of you for that from the bottom of my heart. Also thanks to High and Near and Team 7. The passion and energy each of you brought to the game meant the world to me. Beyond NA, I want to thank God for inspiration, vision, and strength, my family, for endless support along the way, the Project Water Parent Association, for guidance and willingness to serve, and Mike Bliss and Ethan Reay, two men who work hard behind the scenes on behalf of NA Project Water.</p>

