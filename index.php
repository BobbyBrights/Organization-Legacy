<p>Next, I am excited to share a link to next year’s recruitment form. If you are interested in helping lead NA Project Water next year, fill out <a href="https://goo.gl/forms/nGv3mxgHLbBOGa8s2" target="_blank">this form</a>. We’d love to get to know you. </p>

<p>Project Water will have four new Directors; Nikhil Behari, Jack Kenna, Griffin McVay, and Margo Weller. They are ready to carry the torch further than I ever could.</p>

